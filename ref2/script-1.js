function calcCost() {
	calcPos = 0;
	var weight = [1152, 288, 72];
	for (i = 0; i < 3; i++) {
		for (j = 0; j < 4; j++) {
			if (btns[i][j] === 1) {
				calcPos += weight[i] * j;
			}
		}
	}

	
	var weight2 = [36, 12, 4, 1]
	for (k = 0; k < 4; k++) {
		calcPos += btnOpt[k][1] * weight2[k];
	}
	if (calcPos === packages[calcPos][0]) {
		estPrice = packages[calcPos][1];
		corePrice = estPrice;
		estPriceStep = packages[calcPos][2] * 0.03;
		priceAdjustPct = Math.round(estPrice / packages[calcPos][2] * 100);
		priceAdjustPct = 0;
		history.pushState(null, null, url + "?(" + priceAdjustPct + ")_" + shortPrice(estPrice));
	}
}

function postPrice(direction) {
	if (sumCheck === 7) {
		if (direction === "up" && estPrice + estPriceStep <= packages[calcPos][2]) {
			estPrice += estPriceStep;
			corePrice = estPrice;
			priceAdjustPct += 3;
		} else if (direction === "down" && estPrice - estPriceStep >= 0) {
			estPrice -= estPriceStep;
			corePrice = estPrice;
			priceAdjustPct -= 3;
		}
		history.pushState(null, null, url + "?(" + priceAdjustPct + ")_" + shortPrice(estPrice));
		
		if (estPrice + estPriceStep > packages[calcPos][2]) {
			history.pushState(null, null, url + "?[" + priceAdjustPct + "]_" + shortPrice(estPrice));
		}
	}
}

function shortPrice(longPrice) {
	longPrice = Math.round(longPrice / 100);
	return Math.trunc(longPrice / 10) + "." + (longPrice - (Math.trunc(longPrice / 10) * 10));
}

